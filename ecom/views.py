        if product_ids=="":
            product_ids=str(pk)
            product=models.Product.objects.get(id=pk)
            
        elif counter.count(str(pk))<product.stock:
                product_ids=product_ids+"|"+str(pk)
                response.set_cookie('product_ids', product_ids)

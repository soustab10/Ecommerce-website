def cart_view(request):
    #for cart counter
    if 'product_ids' in request.COOKIES:
        product_ids = request.COOKIES['product_ids']
        counter=product_ids.split('|')
        product_count_in_cart=len(list(counter))
    else:
        product_count_in_cart=0

    # fetching product details from db whose id is present in cookie
    products=None
    total=0
    product_id_in_cart=[]
    zipped=[]
    product_count=[]
    if 'product_ids' in request.COOKIES:
        product_ids = request.COOKIES['product_ids']
        if product_ids != "":
            product_id_in_cart=product_ids.split('|')
            products=models.Product.objects.all().filter(id__in = product_id_in_cart)
            product_id_in_cart.sort()
            product_counts = {}
            for element in product_id_in_cart:
                if element in product_counts:
                    product_counts[element] += 1
                else:
                    product_counts[element] = 1
            product_count=list(product_counts.values())
            zipped=zip(products,product_count)
            #for total price shown in cart
            for i,p in enumerate(products):
                total=total+p.price*product_count[i]

    return render(request,'ecom/cart.html',{'zipped':zipped,'total':total,'product_count_in_cart':product_count_in_cart,'product_count':product_count})


def remove_from_cart_view(request,pk):
    #for counter in cart
    if 'product_ids' in request.COOKIES:
        product_ids = request.COOKIES['product_ids']
        counter=product_ids.split('|')
        product_count_in_cart=len(list(counter))
    else:
        product_count_in_cart=0

    # removing product id from cookie
    total=0
    if 'product_ids' in request.COOKIES:
        product_ids = request.COOKIES['product_ids']
        product_id_in_cart=product_ids.split('|')
        
        product_id_in_cart.remove(str(pk))
        product_id_in_cart.sort()
        products=models.Product.objects.all().filter(id__in = product_id_in_cart)
        product_counts = {}
        for element in product_id_in_cart:
            if element in product_counts:
                product_counts[element] += 1
            else:
                product_counts[element] = 1
        product_count=list(product_counts.values())
        zipped=zip(products,product_count)
        #for total price shown in cart
        for i,p in enumerate(products):
            total=total+p.price*product_count[i]

        #  for update coookie value after removing product id in cart
        value=""
        for i in range(len(product_id_in_cart)):
            if i==0:
                value=value+product_id_in_cart[0]
            else:
                value=value+"|"+product_id_in_cart[i]
        response = render(request, 'ecom/cart.html',{'zipped':zipped,'products':products,'total':total,'product_count_in_cart':product_count_in_cart})
        if value=="":
            response.delete_cookie('product_ids')
        response.set_cookie('product_ids',value)
        return response


